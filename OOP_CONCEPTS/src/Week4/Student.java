package Week4;

public class Student {
	
	//Data Member
	private String name;
	private String email;
	
	//Constructor
	public Student() {
		this.name = "Unassiged";
		this.email = "Unassiged";
	}

	//Get & Set Method
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
	

	

}
